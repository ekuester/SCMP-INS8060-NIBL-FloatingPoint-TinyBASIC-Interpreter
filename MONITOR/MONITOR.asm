L FUNCTION VAL16, (VAL16 & 0xFF)
H FUNCTION VAL16, ((VAL16 >> 8) & 0xFF)

JS    MACRO  P,VAL	    ;JUMP TO SUBROUTINE
	LDI     H(VAL-1)
	XPAH    P
	LDI     L(VAL-1)
	XPAL    P
	XPPC    P
	ENDM

LDPI  MACRO  P,VAL	    ;LOAD POINTER
	LDI     L(VAL)
	XPAL    P
	LDI     H(VAL)
	XPAH    P
      ENDM

CALL  MACRO  P,VAL	    ;CALL SUBROUTINE
	XPPC    P
	DB	H(VAL)
	DB	L(VAL)
      ENDM

RTRN  MACRO     P,VAL		;RETURN FROM SUBROUTINE
	XPPC    P
	DB	0
      ENDM

JMPBIT  =  0x80
TSTBIT  =  0x40			;I.L. INSTRUCTION FLAGS
CALBIT  =  0x20
JMPBITH =  JMPBIT*256
TSTBITH =  TSTBIT*256
CALBITH =  CALBIT*256

; SC/MP POINTER ASSIGNMENTS
P1	=  1
P2	=  2
P3	=  3

; THE EXTENSION REGISTER
EREG	=  -128

; DISPLACEMENTS FOR STACK USED BY MONITOR
S_P3LO	=  -1	; STORAGE P3 LOW
S_P3HI	=  -2	; STORAGE P3 HIGH
LIBULO	=  -3	; LOW BYTE LINEBUFFER
S_P1LO	= -15	; STORAGE P1 LOW
S_P1HI	= -16	; STORAGE P1 HIGH
S_LINES	= -17	; LINE COUNTER
S_BYTES	= -18	; BYTE COUNTER
S_PAGE  = -98	; X'9E

; SUPERVISOR JUMPS USING P3 OFFSETTING
SV_STKHI	= (STKHI -SPRVSR +1)	; STORAGE FOR STACKPOINTER HIGH
SV_RESTRT	= (RESTRT -SPRVSR-1)	; X'C01E
SV_RTRN		= (RTRN -SPRVSR	-1)	; X'C06E
SV_RTN1		= (RTN1 -SPRVSR	-1)	; X'C072
SV_NSTERR	= (NSTERR -SPRVSR-1)	; X'C0AD
SV_HEXERR	= (HEXERR -SPRVSR-1)	; X'C0B1
SV_BRK		= (BRK	-SPRVSR-1)	; X'C0B5
SV_SNTERR	= (SNTERR -SPRVSR-1)	; X'C0B9
SV_ERROUT	= (ERROUT -SPRVSR-1)	; X'C0BB

TSTSTR  MACRO  FAIL,A,B
	  DB	H(FAIL - TSTBITH)
	  DB	L(FAIL)
	IFB	B
	  DB    A |0x80 
	ELSE
	  DB    A 
	  DB    B |0x80
	ENDIF
	ENDM

MESG	MACRO  A,B
	DB	A
	DB	B|0x80
	ENDM

GOTO	MACRO  ADR
	DB	H(ADR - JMPBITH)
	DB	L(ADR)
	ENDM
 
DO	MACRO	ADR
	IFNB	ADR 
	DB	H(ADR)
	DB	L(ADR)
	SHIFT
	DO	ALLARGS
	ENDIF
	ENDM 

	ORG	BASE
START:	NOP
	DINT
	LDI	L(STACK)
	XPAL	P2
STKHI:	LDI	H(STACK)
	XPAH	P2
	LDI	0x20
	ST	-29(P2)
	LDPI	P3, SPRVSR	;POINT P3 AT SUPERVISOR
	CALL	P3,ASCOUT
	MESG	"\12  MONITO",'R'
