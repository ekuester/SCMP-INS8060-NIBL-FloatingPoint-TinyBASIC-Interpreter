	CALL	P3,PUTASC
	JP	PRASC
	LD	-29(P2)
	XPAL	P2
	LD	-03(P2)
	XPAL	P1
	ST	+01(P2)
	LD	-04(P2)
	XPAH	P1
	ST	+00(P2)
	LDI	L(STACK)
	XPAL	P2
	RTRN P3
