	ST	-29(P2)
	LDI	0x60
	ST	-03(P2)
	LDI	0xFF
	ST	-12(P2)
	LDI	0x04
	ST	+00(P2)
	LDI	L(BEGIN)	; LOAD LOW OF BEGIN
