	CALL	P3,PUTASC
PRERR:	LD	@1(P1)
	CALL	P3,PUTASC
	JP	PRERR
	LDI	L(M_ERR)
	XPAL	P1
	JP	ERR
	ILD	-12(P2)
	JZ	SV_RESTRT(P3)
	CALL	P3,0xE03E	; RTNERR

