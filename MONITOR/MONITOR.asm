	XPAL	P3
	LD	S_P3HI(P2)	; LOAD P3 FROM STACK
	ORI	0xC0		; SET TWO HIGH BITS
	XPAH	P3
NXT:	LD	(P3)		; LOAD BYTE AFTER 3F INSTRUCTION
	JZ	XCUTE		; IF ZERO RETURN
	JP	GTO		; GOTO COMMAND
	ANI	0x40		; IS TSTSTR COMMAND?
	JNZ	SUPRVS		; YES, JUMP TO SUPERVISOR
GTO:	LD	+01(P3)
	ST	S_P3LO(P2)
	LD	@+02(P3)
	ST	S_P3HI(P2)
	JP	XCT		; to $C03E
	LD	+00(P3)
	XRE
	JNZ	XCT		; to $C03E
	LDI	0x69
	XPAL	P3
	XPAL	P1
	LD	S_PAGE(P2)	; LOAD PAGE FROM STACK WITH OFFSET
	XPAH	P3
	XPAH	P1
	JMP	NXT		; to $C046
	DO	TXTOUT
	NOP
	NOP
	NOP
RTRN:	ILD	-29(P2)
	ILD	-29(P2)
RTN1:	XPAL	P2
RTN2:	LD	S_P3LO(P2)
	XPAL	P3
	LD	S_P3HI(P2)
	XPAH	P3
	LDI	L(STACK)
	XPAL	P2
XCTE:	LD	@-01(P3)
XCUTE:	LD	-96(P2)
SPRVSR:	XPPC	P3
	ST	-96(P2)
	LD	+01(P3)
	JZ	RTRN
	LD	@+01(P3)
	LD	-29(P2)
	XPAL	P2
SUPRVS:	LD	@+02(P3)
	ST	S_P3HI(P2)
CALL:	LD	-01(P3)
	XPAL	P3
	ST	S_P3LO(P2)
	LD	S_P3HI(P2)
	XPAH	P3
	ST	S_P3HI(P2)
	LDI	L(STACK)
	XPAL	P2
	DLD	-29(P2)
	DLD	-29(P2)
	JNZ	XCTE
	LDI	L(SPRVSR)
	XPAL	P3
	LD	S_PAGE(P2)	; LOAD PAGE FROM STACK WITH OFFSET
	XPAH	P3
	LDI	0x1C
	ST	-29(P2)
NSTERR:	LDI	0x56
	JMP	ERROUT
HEXERR:	LDI	L(M_HEX)
	JMP	ERROUT
BRK:	LDI	L(M_BRK)
	JMP	ERROUT
SNTERR:	LDI	L(M_SNTX)
ERROUT:	XPAL	P1
	LDI	H(MESGS)	; ERROR MESSAGES HIGH
	XPAH	P1
